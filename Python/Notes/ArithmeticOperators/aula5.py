'''
+ (soma)
- (subtração)
* (multiplicação)
/ (divisão)
// (Divisão real (arredonda))
** (potência)
% (modulo (resto de divisão))
'''
print('Soma - ', 10+10)
print('Subtração - ', 10-10)
print('Multiplicação - ', 10*10)
print('Divisão - ', 10/10)
print('Divisão Real - ', 10//3)
print('Potêcia - ', 10**10)
print('Modulo - ', 10%9)

